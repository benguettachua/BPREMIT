CREATE TABLE BPREMIT(
   MESSAGE_ID CHAR(15) PRIMARY KEY NOT NULL,
   UMM_TYPE TEXT,
   PUBLICATION_DATE_AND_TIME DATETIME,
   TYPE_OF_EVENT TEXT ,
   EVENT_START DATETIME,
   EVENT_STOP DATETIME,
   EVENT_STATUS CHAR(8),
   AFFECTED_ASSET_OR_UNIT_OR_EIC TEXT,
   BIDDING_ZONE_OR_BALANCING_ZONE TEXT,
   TYPE_OF_UNAVAILABILITY CHAR(9),
   UNAVAILABLE_CAPACITY FLOAT,
   AVAILABLE_CAPACITY FLOAT,
   INSTALLED_CAPACITY_OR_TECHNICAL_CAPACITY FLOAT,
   UNIT_OF_MEASUREMENT CHAR(10),
   REASON_FOR_THE_UNAVAILABILITY TEXT,
   REMARKS TEXT,
   FUEL_TYPE CHAR(10),
   MARKET_PARTICIPATION_OR_EIC TEXT,
   MESSAGE_ID_NUM INT
);